"""Provide stable ordering for mixed numeric and textual Comfy node ids."""

from __future__ import annotations


def natural_node_key(value: str) -> tuple[int, str]:
    """Sort numeric ids numerically before falling back to lexical sorting."""

    try:
        return (0, f"{int(value):010d}")
    except ValueError:
        return (1, value)
